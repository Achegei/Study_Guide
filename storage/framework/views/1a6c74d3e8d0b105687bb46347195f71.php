                <div class="prose max-w-none text-gray-800 leading-relaxed">
                    <h2>1. Geography and Natural Features</h2>
                    <p>Manitoba sits at the center of Canada, bordered by Saskatchewan, Ontario, Nunavut, and the U.S. state of North Dakota. The province stretches from farmland plains in the south to Arctic tundra in the far north.</p>
                    <p>It features over 100,000 lakes, including Lake Winnipeg (the 6th largest freshwater lake in Canada), as well as Lake Manitoba and Lake Winnipegosis. Manitoba’s terrain includes flat grasslands, wetlands, boreal forests, and subarctic regions.</p>
                    <p>Its climate is marked by cold, snowy winters and warm, humid summers, especially in the southern part.</p>
                    <br>

                    <h2>2. Indigenous Heritage and Early Peoples</h2>
                    <p>Manitoba has been home to Indigenous peoples for over 10,000 years. Major First Nations groups include the Cree, Ojibwe, Dene, Dakota, and Inuit in the northern regions.</p>
                    <p>The Red River region became central to the development of the Métis Nation, descendants of European fur traders and Indigenous women. The Métis played a pivotal role in the province’s founding and are central to its identity.</p>
                    <p>Oral traditions, hunting, fishing, and land stewardship have been passed down for generations and continue today.</p>
                    <br>

                    <h2>3. Fur Trade and European Settlement</h2>
                    <p>In the 1600s and 1700s, the fur trade brought French and British explorers and traders to the region. The Hudson’s Bay Company established trading posts such as York Factory and Fort Garry.</p>
                    <p>The Red River Settlement grew under Lord Selkirk’s sponsorship in the early 1800s, creating tension with local Métis communities. Louis Riel, a Métis leader, emerged during this period and would later become one of the most significant political figures in Canadian history.</p>
                    <br>

                    <h2>4. Provincehood and Entry into Confederation</h2>
                    <p>Manitoba entered Confederation on July 15, 1870, through the Manitoba Act, passed in response to the Red River Resistance led by Louis Riel.</p>
                    <p>This marked the birth of the province as a bilingual and multicultural society, guaranteeing language and land rights for Métis and French-speaking citizens.</p>
                    <p>Though these rights were not always respected in practice, the legacy of the Red River Resistance remains central to Manitoba’s founding story.</p>
                    <br>

                    <h2>5. Population and Demographics</h2>
                    <p>As of 2025, Manitoba’s population exceeds 1.4 million. The majority live in the southern part of the province, with Winnipeg (the capital and largest city) home to more than half the provincial population.</p>
                    <p>Manitoba is incredibly diverse:</p>
                    <ul>
                        <li>Indigenous peoples make up approximately 18% of the population — among the highest in Canada.</li>
                        <li>Growing newcomer communities from the Philippines, India, China, Nigeria, and Ukraine are expanding Winnipeg’s cultural footprint.</li>
                        <li>Francophone Manitobans make up about 4% of the population, with Saint-Boniface serving as the cultural hub for French-speaking communities.</li>
                    </ul>
                    <br>

                    <h2>6. Cities and Communities</h2>
                    <p><strong>Winnipeg</strong></p>
                    <ul>
                        <li>Capital and economic heart of the province.</li>
                        <li>Hosts the Canadian Museum for Human Rights, Manitoba Legislature, and Forks Market.</li>
                        <li>Diverse neighborhoods include Filipino, Indigenous, Punjabi, French, and African communities.</li>
                    </ul>
                    <p><strong>Brandon</strong></p>
                    <ul>
                        <li>Manitoba’s second-largest city, known for agriculture, military history, and post-secondary institutions.</li>
                    </ul>
                    <p><strong>Northern Communities</strong></p>
                    <ul>
                        <li>Communities like Thompson, Churchill, and The Pas contribute to mining, northern culture, and Arctic tourism.</li>
                    </ul>
                    <br>

                    <h2>7. Economy and Key Industries</h2>
                    <p>Manitoba has a mixed and stable economy, built around several core sectors:</p>
                    <ul>
                        <li>Agriculture: Major producer of wheat, barley, oats, canola, hogs, and poultry.</li>
                        <li>Manufacturing: Aerospace (Winnipeg is a key hub), food processing, and bus production (e.g., New Flyer Industries).</li>
                        <li>Hydroelectricity: Manitoba Hydro generates nearly all of the province’s power from clean water sources.</li>
                        <li>Mining: Northern Manitoba is rich in nickel, copper, and other minerals.</li>
                        <li>Tourism: Includes Arctic wildlife tours (especially polar bear viewing in Churchill), lake tourism, and cultural festivals.</li>
                    </ul>
                    <br>

                    <h2>8. Culture and Traditions</h2>
                    <p>Manitoba celebrates its cultural richness in many ways:</p>
                    <ul>
                        <li>Métis and Indigenous cultures are widely recognized through festivals, music, art, and education.</li>
                        <li>Annual events like Festival du Voyageur celebrate French-Canadian heritage.</li>
                        <li>Folklorama in Winnipeg showcases more than 40 ethnic communities through music, dance, and cuisine.</li>
                        <li>Local artists, musicians, and writers often reflect prairie themes, social justice, and multicultural stories.</li>
                    </ul>
                    <p>Famous Manitobans include Louis Riel, Gabrielle Roy, Neil Young, and Cynthia Ozick.</p>
                    <br>

                    <h2>9. Political Contributions and Government</h2>
                    <p>Manitoba operates under a parliamentary system like other provinces:</p>
                    <ul>
                        <li>Premier: Head of the provincial government.</li>
                        <li>Legislative Assembly: Located in the capital, Winnipeg.</li>
                        <li>Lieutenant Governor: The King’s representative in Manitoba.</li>
                    </ul>
                    <p>Historically, Manitoba played a major role in civil rights. In 1916, it became the first province to grant women the right to vote in provincial elections.</p>
                    <p>Louis Riel, though controversial in his time, is now recognized as a Father of Confederation for his role in negotiating Manitoba’s entry into Canada.</p>
                    <br>

                    <h2>10. Indigenous Reconciliation in Manitoba</h2>
                    <p>Manitoba is home to over 60 First Nations and numerous Métis and Inuit communities. The province is actively involved in:</p>
                    <ul>
                        <li>Truth and Reconciliation initiatives</li>
                        <li>Restoring Indigenous languages</li>
                        <li>Creating land-based learning opportunities</li>
                        <li>Expanding health and education services for Indigenous peoples</li>
                    </ul>
                    <p>The National Centre for Truth and Reconciliation is located at the University of Manitoba, playing a leading role in documenting residential school history and advancing justice.</p>
                    <br>

                    <h2>11. Religion and Freedom</h2>
                    <p>Manitoba’s population practices a variety of faiths:</p>
                    <ul>
                        <li>Christianity is the largest religion.</li>
                        <li>Islam, Sikhism, Hinduism, Buddhism, Judaism, and Indigenous spiritual traditions are growing.</li>
                        <li>Manitoba also has a high percentage of people identifying with no religion.</li>
                    </ul>
                    <p>Religious freedom is constitutionally protected, and places of worship are found in most towns and cities.</p>
                    <br>

                    <h2>12. Law, Order, and Public Institutions</h2>
                    <p>Manitoba’s legal system is rooted in English common law. The Court of King’s Bench and Court of Appeal handle most legal matters.</p>
                    <p>Policing is handled by:</p>
                    <ul>
                        <li>Winnipeg Police Service</li>
                        <li>RCMP in smaller towns and rural areas</li>
                    </ul>
                    <p>Public services include health care, K–12 education, post-secondary institutions, public housing, and employment programs.</p>
                    <br>

                    <h2>13. Education and Innovation</h2>
                    <p>Manitoba offers publicly funded education in English and French. Indigenous-led schools and immersion programs are expanding.</p>
                    <p>Post-secondary institutions include:</p>
                    <ul>
                        <li>University of Manitoba</li>
                        <li>University of Winnipeg</li>
                        <li>Brandon University</li>
                        <li>Red River College Polytechnic</li>
                    </ul>
                    <p>Academic programs are strong in human rights, agriculture, Indigenous studies, climate science, and renewable energy.</p>
                    <br>

                    <h2>14. Symbols and Provincial Identity</h2>
                    <ul>
                        <li>Flag: Red ensign featuring the Cross of Saint George and Manitoba’s coat of arms.</li>
                        <li>Floral emblem: Prairie crocus</li>
                        <li>Motto: “Gloriosus et liber” (Glorious and free)</li>
                        <li>Coat of arms: Includes a bison and the British lion</li>
                    </ul>
                    <p>These symbols appear in schools, documents, and public institutions across Manitoba.</p>
