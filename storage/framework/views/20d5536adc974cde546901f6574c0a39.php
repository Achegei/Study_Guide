                <div class="prose max-w-none text-gray-800 leading-relaxed">
                    <h2>1. Geography and Location</h2>
                    <p>New Brunswick is located in Eastern Canada, part of the Maritime provinces alongside Nova Scotia and Prince Edward Island. It is bordered by Québec to the north, Nova Scotia and the Bay of Fundy to the south, and the U.S. state of Maine to the west.</p>
                    <p>Key features include:</p>
                    <ul>
                        <li>A rugged Atlantic coastline with rocky shores and fishing villages</li>
                        <li>Dense inland forests and highlands</li>
                        <li>Fertile river valleys, especially along the Saint John River</li>
                        <li>The Bay of Fundy, famous for having the highest tides in the world</li>
                    </ul>
                    <p>New Brunswick’s mix of forest, ocean, rivers, and mountains has shaped its economy, transportation, and traditions.</p>
                    <br>

                    <h2>2. Indigenous History and First Peoples</h2>
                    <p>Long before European contact, New Brunswick was home to Indigenous peoples — especially the Mi’kmaq, Maliseet (Wolastoqiyik), and Passamaquoddy nations.</p>
                    <p>These groups:</p>
                    <ul>
                        <li>Lived off the land through fishing, hunting, and seasonal migration</li>
                        <li>Navigated rivers and coastlines in birchbark canoes</li>
                        <li>Held deep spiritual and ecological knowledge of the region</li>
                    </ul>
                    <p>Today, more than 15 First Nations communities live across the province, preserving their cultures, languages, and governance systems.</p>
                    <br>

                    <h2>3. French and British Settlement</h2>
                    <p>New Brunswick was originally part of Acadia, the early French colony in the Maritimes. Acadians lived in close contact with Mi’kmaq allies, building dikes, farming marshlands, and fishing the coastal waters.</p>
                    <p>In the 1750s, the British deported many Acadians in a mass expulsion known as Le Grand Dérangement. Some returned after years of exile, rebuilding their lives in the province. Their resilience is central to Acadian identity.</p>
                    <p>Following British control, Loyalist refugees from the United States arrived in large numbers after the American Revolution, founding Saint John and other English-speaking settlements.</p>
                    <br>

                    <h2>4. Confederation and Bilingual Foundations</h2>
                    <p>New Brunswick joined Confederation in 1867, as one of the original four provinces.</p>
                    <p>It is unique because:</p>
                    <ul>
                        <li>It is the only officially bilingual province in Canada</li>
                        <li>English and French communities coexist across the province</li>
                        <li>Bilingual services, education, and media are legally protected</li>
                    </ul>
                    <p>New Brunswick passed its Official Languages Act in 1969, ensuring equal status for English and French in all government services. This model has become a national example of linguistic inclusion.</p>
                    <br>

                    <h2>5. Cities and Communities</h2>
                    <p><strong>Fredericton</strong></p>
                    <ul>
                        <li>The capital city</li>
                        <li>Known for public service, arts, and universities</li>
                    </ul>
                    <p><strong>Moncton</strong></p>
                    <ul>
                        <li>Largest urban centre</li>
                        <li>Commercial, transport, and bilingual hub</li>
                        <li>A centre of Acadian culture</li>
                    </ul>
                    <p><strong>Saint John</strong></p>
                    <ul>
                        <li>Historic port city</li>
                        <li>Site of Loyalist landing and early industrial growth</li>
                    </ul>
                    <p><strong>Edmundston, Bathurst, and Campbellton</strong></p>
                    <ul>
                        <li>French-speaking northern towns with deep Acadian and forestry roots</li>
                    </ul>
                    <p>New Brunswick’s population is mostly rural or in small towns. Community ties, bilingualism, and multicultural identity define everyday life.</p>
                    <br>

                    <h2>6. Language and Identity</h2>
                    <p>Approximately:</p>
                    <ul>
                        <li>65% of the population speaks English as a first language</li>
                        <li>32% speak French, mainly Acadian French</li>
                        <li>A growing number of newcomers speak other languages and attend French immersion or ESL programs</li>
                    </ul>
                    <p>The province maintains two parallel school systems:</p>
                    <ul>
                        <li>One in English</li>
                        <li>One in French</li>
                    </ul>
                    <p>Acadian music, literature, and festivals are widely celebrated, especially in the Acadian Peninsula and Moncton region.</p>
                    <br>

                    <h2>7. Economy and Major Industries</h2>
                    <p>New Brunswick has a resource-based economy transitioning toward services and innovation. Key sectors include:</p>
                    <ul>
                        <li>Forestry: Lumber, pulp, and paper production from vast forests</li>
                        <li>Fisheries: Lobster, crab, scallops, and aquaculture (especially salmon farming)</li>
                        <li>Agriculture: Potatoes, blueberries, and dairy</li>
                        <li>Energy: Hydroelectricity, wind, and oil refining (Saint John houses Canada’s largest oil refinery)</li>
                        <li>Tourism: Coastal parks, whale watching, and historic sites</li>
                        <li>Call centres and bilingual services: A growing employer due to strong language skills</li>
                    </ul>
                    <p>The government promotes economic diversification, tech adoption, and rural development.</p>
                    <br>

                    <h2>8. Education and Public Services</h2>
                    <p>Education is delivered in two official languages. School boards operate independently but under provincial curriculum guidelines.</p>
                    <p>Post-secondary institutions include:</p>
                    <ul>
                        <li>University of New Brunswick (UNB) – campuses in Fredericton and Saint John</li>
                        <li>Université de Moncton – largest French-language university outside Québec</li>
                        <li>Mount Allison University – known for undergraduate excellence</li>
                    </ul>
                    <p>Bilingual health care and legal services are widely accessible, especially in larger centres.</p>
                    <br>

                    <h2>9. Political and Legal Systems</h2>
                    <p>New Brunswick is governed through a parliamentary democracy:</p>
                    <ul>
                        <li>Premier leads the provincial government</li>
                        <li>Lieutenant Governor represents the King</li>
                        <li>The Legislative Assembly meets in Fredericton</li>
                    </ul>
                    <p>Politically, New Brunswick has shifted between Liberal and Progressive Conservative governments, with recent increases in Green and bilingual advocacy.</p>
                    <p>The province has a Court of Queen’s Bench, Provincial Court, and bilingual legal proceedings.</p>
                    <br>

                    <h2>10. Indigenous Rights and Reconciliation</h2>
                    <p>Indigenous peoples in New Brunswick are working toward:</p>
                    <ul>
                        <li>Land recognition and treaty education</li>
                        <li>Language recovery (Mi’kmaq and Maliseet)</li>
                        <li>Increased self-governance and representation</li>
                        <li>Rebuilding culture through education, ceremonies, and community investment</li>
                    </ul>
                    <p>Collaboration with First Nations continues on:</p>
                    <ul>
                        <li>Resource sharing agreements</li>
                        <li>Health equity</li>
                        <li>Education inclusion</li>
                    </ul>
                    <p>The province acknowledges the importance of Truth and Reconciliation efforts and residential school awareness.</p>
                    <br>

                    <h2>11. Cultural Contributions and Acadian Pride</h2>
                    <p>New Brunswick is a stronghold of Acadian culture, which includes:</p>
                    <ul>
                        <li>Le Congrès mondial acadien – an international Acadian gathering held every five years</li>
                        <li>Acadian flag: Blue, white, and red with a gold star</li>
                        <li>Acadian music, storytelling, cuisine, and dialects</li>
                    </ul>
                    <p>Major festivals include:</p>
                    <ul>
                        <li>Fête nationale de l’Acadie (August 15)</li>
                        <li>Harvest Jazz & Blues Festival (Fredericton)</li>
                        <li>Salmon Festival (Campbellton)</li>
                    </ul>
                    <p>The province celebrates both Anglophone and Francophone heritages, fostering inclusion and community.</p>
                    <br>

                    <h2>12. Immigration and Modern Diversity</h2>
                    <p>Though historically shaped by Acadians and Loyalists, New Brunswick is welcoming more immigrants from:</p>
                    <ul>
                        <li>Philippines</li>
                        <li>India</li>
                        <li>Syria</li>
                        <li>Nigeria</li>
                        <li>Latin America</li>
                    </ul>
                    <p>Newcomers bring diversity in:</p>
                    <ul>
                        <li>Faith traditions (Christian, Muslim, Hindu, Sikh)</li>
                        <li>Languages and cultural practices</li>
                        <li>Entrepreneurship and labour market participation</li>
                    </ul>
                    <p>Government supports include:</p>
                    <ul>
                        <li>Francophone immigration programs</li>
                        <li>Regional settlement services</li>
                        <li>Language classes (English and French)</li>
                    </ul>
                    <br>

                    <h2>13. Symbols and Provincial Identity</h2>
                    <ul>
                        <li>Flag: Features a golden lion and a galley ship, symbolizing the province’s royal heritage and maritime economy</li>
                        <li>Floral Emblem: Purple violet</li>
                        <li>Tree: Balsam fir</li>
                        <li>Motto: Spem reduxit – “Hope was restored”</li>
                    </ul>
                    <p>These symbols reflect resilience, diversity, and maritime pride.</p>
