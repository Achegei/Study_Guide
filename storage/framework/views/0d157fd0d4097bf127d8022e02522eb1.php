                    <h2>Chapter 4 – The Government and Political System</h2>
                    <h3>4.1 Canada’s Constitutional Monarchy</h3>
                    <p>Canada is both a constitutional monarchy and a parliamentary democracy.</p>
                    <ul>
                        <li><strong>Head of State:</strong> His Majesty King Charles III.</li>
                        <li><strong>Governor General:</strong> The King’s representative at the federal level.</li>
                        <li><strong>Lieutenant Governors:</strong> Represent the King in each province.</li>
                    </ul>
                    <p>The monarchy’s role is symbolic and ceremonial, while the day-to-day decisions are made by elected governments.</p>
                    <br>

                    <h3>4.2 The Federal System of Government</h3>
                    <p>Canada has three levels of government, each responsible for different areas:</p>
                    <ol>
                        <li><strong>Federal Government</strong> – Handles national matters such as:
                            <ul>
                                <li>National defense</li>
                                <li>Foreign policy</li>
                                <li>Immigration and citizenship</li>
                                <li>Currency and banking</li>
                            </ul>
                        </li>
                        <li><strong>Provincial/Territorial Governments</strong> – Manage:
                            <ul>
                                <li>Education</li>
                                <li>Healthcare</li>
                                <li>Natural resources</li>
                                <li>Highways</li>
                            </ul>
                        </li>
                        <li><strong>Municipal Governments</strong> – Operate in cities, towns, and districts, handling:
                            <ul>
                                <li>Water and waste services</li>
                                <li>Local policing and fire services</li>
                                <li>Public transit</li>
                                <li>Local bylaws</li>
                            </ul>
                        </li>
                    </ol>
                    <br>

                    <h3>4.3 Parliament</h3>
                    <p>Parliament is Canada’s legislative branch and has two chambers:</p>
                    <ul>
                        <li><strong>House of Commons</strong> – Members of Parliament (MPs) are elected by citizens to represent ridings (electoral districts). They introduce, debate, and vote on laws.</li>
                        <li><strong>Senate</strong> – Senators are appointed by the Governor General on the Prime Minister’s advice. They review and refine legislation passed by the House of Commons.</li>
                    </ul>
                    <br>

                    <h3>4.4 Prime Minister and Cabinet</h3>
                    <ul>
                        <li><strong>Prime Minister (PM):</strong> The head of government and leader of the political party with the most seats in the House of Commons.</li>
                        <li><strong>Cabinet Ministers:</strong> Chosen by the PM, each is responsible for a specific department (e.g., health, finance, defense). Cabinet decisions are binding on all ministers — this is called cabinet solidarity.</li>
                    </ul>
                    <br>

                    <h3>4.5 How Elections Work</h3>
                    <p>Federal elections are held at least every four years, though they can occur earlier if Parliament is dissolved.</p>
                    <ul>
                        <li>Citizens aged 18 and older can vote in federal, provincial/territorial, and municipal elections.</li>
                        <li>Canada uses a <strong>first-past-the-post</strong> voting system: the candidate with the most votes in a riding wins, even without a majority.</li>
                    </ul>
                    <p>📌 Test Tip: Voting is a right and a civic duty in Canada — low voter turnout weakens democracy.</p>
                    <br>

                    <h3>4.6 Political Parties</h3>
                    <p>Political parties group people with similar political ideas and goals. At the federal level, the main parties include:</p>
                    <ul>
                        <li>Liberal Party</li>
                        <li>Conservative Party</li>
                        <li>New Democratic Party (NDP)</li>
                        <li>Bloc Québécois (operates only in Quebec)</li>
                        <li>Green Party</li>
                    </ul>
                    <p>Smaller parties may also run candidates but rarely form governments.</p>
                    <br>

                    <h3>4.7 The Role of the Public Service</h3>
                    <p>The public service is made up of non-partisan government employees who carry out the day-to-day work of running the country. They remain in their roles even when a new government is elected.</p>
                    <br>

