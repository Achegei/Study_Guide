                    <h2>Chapter 8 – Culture and Identity</h2>
                    <h3>8.1 Foundations of Canadian Identity</h3>
                    <p>Canada’s identity is shaped by Indigenous heritage, French and British traditions, and waves of immigration from every continent.</p>
                    <p>It is a nation where different languages, religions, and traditions coexist peacefully, united by values of democracy, equality, and the rule of law.</p>
                    <p>✔ Key Fact: Canadian identity is not based on a single culture or ethnicity — it’s defined by shared values and respect for diversity.</p>
                    <br>

                    <h3>8.2 Official Languages and Bilingualism</h3>
                    <p>Canada has two official languages: <strong>English and French</strong>.</p>
                    <ul>
                        <li><strong>French in Canada</strong> – Quebec is the heart of Canada’s French-speaking population, but francophone communities also exist in New Brunswick, Ontario, and Manitoba.</li>
                        <li><strong>English in Canada</strong> – Spoken in every province and territory; the majority language in most regions.</li>
                        <li><strong>Bilingualism Policy</strong> – Federal government services, court proceedings, and communications are available in both English and French nationwide.</li>
                    </ul>
                    <p>📌 Test Tip: New Brunswick is Canada’s only officially bilingual province.</p>
                    <br>

                    <h3>8.3 Indigenous Cultures</h3>
                    <p>Indigenous peoples — First Nations, Inuit, and Métis — are central to Canada’s cultural fabric. Their contributions include:</p>
                    <ul>
                        <li><strong>Art:</strong> Totem pole carving, beadwork, and traditional paintings.</li>
                        <li><strong>Music:</strong> Inuit throat singing, Métis fiddle tunes, and powwow drumming.</li>
                        <li><strong>Language:</strong> Dozens of Indigenous languages, many being revitalized today.</li>
                        <li><strong>Environmental stewardship:</strong> Traditional knowledge of land and wildlife management.</li>
                    </ul>
                    <br>

                    <h3>8.4 Arts and Literature</h3>
                    <p>Canada’s arts scene is internationally respected:</p>
                    <ul>
                        <li><strong>Literature:</strong> Margaret Atwood, Alice Munro (Nobel Prize winner), and Joseph Boyden explore Canadian themes.</li>
                        <li><strong>Music:</strong> Céline Dion, Drake, The Weeknd, Shania Twain, and countless local artists.</li>
                        <li><strong>Visual Arts:</strong> The Group of Seven painters captured the landscapes of Canada.</li>
                        <li><strong>Film & TV:</strong> Major production hubs in Toronto, Vancouver, and Montreal attract Hollywood films and international TV series.</li>
                    </ul>
                    <p>✔ Key Fact: Many “Hollywood” movies are actually filmed in Canada.</p>
                    <br>

                    <h3>8.5 Sports in Canadian Life</h3>
                    <p>Sports unite communities and reflect Canadian culture:</p>
                    <ul>
                        <li><strong>National Sports:</strong> Ice hockey (winter) and lacrosse (summer).</li>
                        <li>Popular Activities: Soccer, basketball, curling, skiing, baseball.</li>
                        <li>International Success: Canada is a top contender in the Winter Olympics, especially in hockey and figure skating.</li>
                    </ul>
                    <br>

                    <h3>8.6 Multiculturalism in Action</h3>
                    <p>Since <strong>1971</strong>, Canada’s official multiculturalism policy has encouraged:</p>
                    <ul>
                        <li>Preservation of cultural traditions.</li>
                        <li>Celebration of cultural festivals and heritage events.</li>
                        <li>Protection against racial, ethnic, and religious discrimination.</li>
                    </ul>
                    <p>Examples of cultural celebrations:</p>
                    <ul>
                        <li><strong>Caribana</strong> in Toronto – Caribbean culture and music.</li>
                        <li>Chinese New Year – Celebrated in many Canadian cities.</li>
                        <li><strong>National Indigenous Peoples Day</strong> – June 21.</li>
                    </ul>
                    <br>

                    <h3>8.7 National Holidays and Celebrations</h3>
                    <ul>
                        <li><strong>Canada Day</strong> – July 1, celebrating Confederation.</li>
                        <li><strong>Remembrance Day</strong> – November 11, honouring veterans.</li>
                        <li>Thanksgiving – Second Monday in October (except in some provinces).</li>
                        <li>Provincial Holidays: Example – Saint-Jean-Baptiste Day in Quebec.</li>
                    </ul>
                    <br>

                    <h3>8.8 Canada’s International Image</h3>
                    <p>Canada is known globally for:</p>
                    <ul>
                        <li>Peacekeeping and humanitarian work.</li>
                        <li>A stable democracy.</li>
                        <li>Respect for human rights and diversity.</li>
                        <li>Environmental leadership in conservation and clean energy.</li>
                    </ul>
