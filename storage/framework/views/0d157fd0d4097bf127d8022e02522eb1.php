                    <h2>Chapter 10 – Regions of Canada</h2>
                    <h3>10.1 Canada’s Regional Diversity</h3>
                    <p>Canada is the second-largest country in the world by land area, stretching from the Atlantic Ocean in the east to the Pacific Ocean in the west, and deep into the Arctic in the north.</p>
                    <p>While Canada has 10 provinces and 3 territories, it is often grouped into five geographic regions based on geography, economy, and culture:</p>
                    <ol>
                        <li>Atlantic Provinces</li>
                        <li>Central Canada</li>
                        <li>Prairie Provinces</li>
                        <li>West Coast</li>
                        <li>The North</li>
                    </ol>
                    <p>✔ Key Fact: These regions are not separate governments — they’re simply a way to describe Canada’s diversity.</p>
                    <br>

                    <h3>10.2 The Atlantic Provinces</h3>
                    <p>Provinces: Newfoundland and Labrador, Prince Edward Island, Nova Scotia, New Brunswick.</p>
                    <ul>
                        <li><strong>Geography:</strong> Rugged coastlines, forested hills, fishing villages, and small farms.</li>
                        <li><strong>Economy:</strong> Historically based on fishing, shipping, and shipbuilding; now includes tourism, energy, and small manufacturing.</li>
                        <li><strong>Culture:</strong> Maritime traditions, Celtic and Acadian heritage, folk music, and storytelling.</li>
                        <li><strong>Climate:</strong> Cool summers, wet and snowy winters, moderated by the ocean.</li>
                    </ul>
                    <p>📌 Test Tip: Halifax, Nova Scotia, is one of Canada’s key Atlantic ports.</p>
                    <br>

                    <h3>10.3 Central Canada</h3>
                    <p>Provinces: Quebec and Ontario.</p>
                    <ul>
                        <li><strong>Geography:</strong> Fertile farmland, large rivers and lakes in the south, and vast forests in the north.</li>
                        <li><strong>Economy:</strong> Canada’s industrial heartland — manufacturing, finance, media, and technology.</li>
                        <li><strong>Culture:</strong> Ontario is highly multicultural; Quebec is the centre of French-speaking culture.</li>
                        <li><strong>Climate:</strong> Cold winters, warm summers, heavy snowfall in many areas.</li>
                    </ul>
                    <p>✔ Key Fact: <strong>Over half of Canada’s population lives in Ontario and Quebec.</strong></p>
                    <br>

                    <h3>10.4 Prairie Provinces</h3>
                    <p>Provinces: Manitoba, Saskatchewan, Alberta.</p>
                    <ul>
                        <li><strong>Geography:</strong> Flat plains, fertile farmland, rolling hills; Rocky Mountains begin in Alberta.</li>
                        <li><strong>Economy:</strong> Agriculture (wheat, canola, cattle), oil and gas, mining, transportation hubs.</li>
                        <li><strong>Culture:</strong> Known for rodeos, community fairs, and rural traditions.</li>
                        <li><strong>Climate:</strong> Cold, dry winters and hot summers; long daylight hours in summer.</li>
                    </ul>
                    <p>📌 Test Tip: Saskatchewan produces over half of Canada’s wheat.</p>
                    <br>

                    <h3>10.5 The West Coast</h3>
                    <p>Province: British Columbia.</p>
                    <ul>
                        <li><strong>Geography:</strong> Coastal mountains, rainforests, and islands.</li>
                        <li><strong>Economy:</strong> Ports, forestry, fishing, film production, and technology. Tourism is a major industry.</li>
                        <li><strong>Culture:</strong> Strong Asian influence, vibrant Indigenous communities, and an outdoor lifestyle.</li>
                        <li><strong>Climate:</strong> Mild, wet winters on the coast; snowy mountains inland.</li>
                    </ul>
                    <p>✔ Key Fact: Vancouver is Canada’s busiest port.</p>
                    <br>

                    <h3>10.6 The North</h3>
                    <p>Territories: Yukon, Northwest Territories, Nunavut.</p>
                    <ul>
                        <li><strong>Geography:</strong> Tundra, Arctic coastline, and vast wilderness.</li>
                        <li><strong>Economy:</strong> Mining (gold, diamonds), oil and gas, tourism, traditional hunting and fishing.</li>
                        <li><strong>Culture:</strong> Home to Inuit, Dene, and other Indigenous communities with deep connections to the land.</li>
                        <li><strong>Climate:</strong> Long, harsh winters; short summers; extreme daylight changes — midnight sun in summer, long darkness in winter.</li>
                    </ul>
                    <p>📌 Test Tip: The North covers over one-third of Canada’s land area but has a very small population.</p>
                    <br>

                    <h3>10.7 Canada’s Capital and Major Cities</h3>
                    <ul>
                        <li><strong>Ottawa</strong> (Ontario) – Canada’s capital; Parliament and national institutions.</li>
                        <li><strong>Toronto</strong> (Ontario) – Largest city and economic hub.</li>
                        <li><strong>Montreal</strong> (Quebec) – Largest French-speaking city in North America.</li>
                        <li><strong>Vancouver</strong> (British Columbia) – Gateway to the Pacific.</li>
                        <li><strong>Calgary</strong> (Alberta) – Energy industry hub.</li>
                        <li><strong>Halifax</strong> (Nova Scotia) – Major Atlantic port.</li>
                        <li><strong>Winnipeg</strong> (Manitoba) – Central transportation and trade centre.</li>
                    </ul>
                    <br>

                    <h3>10.8 How Regions Work Together</h3>
                    <p>Canada’s regions are economically and culturally interconnected:</p>
                    <ul>
                        <li>Prairie grain shipped through West Coast and Atlantic ports.</li>
                        <li>Alberta and Newfoundland energy supports industries in Ontario and Quebec.</li>
                        <li>Tourism flows across all provinces and territories.</li>
                    </ul>
