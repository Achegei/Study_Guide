                    <h2>Chapter 5 – Rights and Responsibilities of Citizenship</h2>
                    <h3>5.1 Balancing Rights and Responsibilities</h3>
                    <p>Canadian citizenship is built on a balance:</p>
                    <ul>
                        <li>Rights protect your freedoms and dignity.</li>
                        <li>Responsibilities ensure Canada remains strong, fair, and democratic.</li>
                    </ul>
                    <p>These two go hand in hand — rights without responsibilities can weaken democracy, while responsibilities without rights can lead to injustice.</p>
                    <p>✔ Key Fact: Many rights are guaranteed under the <strong>Canadian Charter of Rights and Freedoms</strong>, part of the Constitution since 1982.</p>
                    <br>

                    <h3>5.2 Rights of Canadian Citizens</h3>
                    <p>While everyone in Canada enjoys certain legal protections, some rights are exclusive to citizens:</p>
                    <ol>
                        <li><strong>Vote in Elections</strong> – At the federal, provincial/territorial, and municipal levels.</li>
                        <li><strong>Run for Public Office</strong> – Stand as a candidate in elections.</li>
                        <li><strong>Hold a Canadian Passport</strong> – Travel with ease and receive Canadian diplomatic support abroad.</li>
                        <li><strong>Equality Before the Law</strong> – Protection from discrimination regardless of background.</li>
                        <li><strong>Language Rights</strong> – Access to federal government services in English or French anywhere in Canada.</li>
                    </ol>
                    <p>📌 Test Tip: Only citizens can vote in federal elections or hold certain government jobs requiring high-level security clearance.</p>
                    <br>

                    <h3>5.3 The Canadian Charter of Rights and Freedoms</h3>
                    <p>The Charter is the cornerstone of rights in Canada. It guarantees:</p>
                    <ul>
                        <li><strong>Fundamental Freedoms</strong> – Freedom of speech, religion, peaceful assembly, and association.</li>
                        <li><strong>Democratic Rights</strong> – The right to vote and run for office.</li>
                        <li><strong>Mobility Rights</strong> – The right to live, work, or study anywhere in Canada and to leave or return freely.</li>
                        <li><strong>Legal Rights</strong> – Protection from unlawful search or detention, and the right to a fair trial.</li>
                        <li><strong>Equality Rights</strong> – Equal protection under the law without discrimination.</li>
                    </ul>
                    <br>

                    <h3>5.4 Responsibilities of Canadian Citizens</h3>
                    <p>Rights come with obligations that help maintain Canada’s democratic system and protect the common good:</p>
                    <ol>
                        <li><strong>Obey the Law</strong> – The rule of law applies equally to everyone.</li>
                        <li><strong>Serve on a Jury</strong> – Ensure fairness in trials when called upon.</li>
                        <li><strong>Vote in Elections</strong> – Participate in shaping Canada’s future.</li>
                        <li><strong>Help Protect the Environment</strong> – Conserve resources and support sustainability.</li>
                        <li><strong>Defend Canada When Needed</strong> – Contribute in times of national emergency (military service is voluntary in peacetime).</li>
                    </ol>
                    <p>✔ Key Fact: While voting is not mandatory, low voter turnout weakens democracy.</p>
                    <br>

                    <h3>5.5 Why Responsibilities Matter</h3>
                    <p>A healthy democracy relies on active and informed citizens. If citizens neglect their duties — especially voting — decisions are left to fewer voices, which may not reflect the majority.</p>
                    <br>

                    <h3>5.6 Real-Life Examples</h3>
                    <ul>
                        <li><strong>Jury Duty:</strong> Serving in a criminal trial to ensure a fair verdict.</li>
                        <li><strong>Voting:</strong> Researching candidates in your riding and casting your ballot.</li>
                        <li><strong>Environmental Protection:</strong> Joining a community clean-up or supporting renewable energy projects.</li>
                    </ul>
