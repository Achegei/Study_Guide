                    <h2>Chapter 3 – Modern Canada: Expansion, War, and Change</h2>
                    <h3>3.1 Expanding the Dominion</h3>
                    <p>After Confederation in 1867, Canada began expanding westward and northward as new provinces and territories joined the union. Each addition brought unique resources, cultures, and challenges.</p>
                    <p>Timeline of Expansion:</p>
                    <ul>
                        <li><strong>1870 – Manitoba:</strong> Created after negotiations with the Métis during the Red River Resistance, led by Louis Riel. The agreement protected Métis land rights and culture.</li>
                        <li><strong>1871 – British Columbia:</strong> Joined on the promise that Canada would build a transcontinental railway within ten years.</li>
                        <li><strong>1873 – Prince Edward Island:</strong> Joined when Canada agreed to take over its railway debts and provide regular ferry service.</li>
                        <li><strong>1898 – Yukon Territory:</strong> Created during the Klondike Gold Rush to maintain law and order in the north.</li>
                        <li><strong>1905 – Alberta and Saskatchewan:</strong> Formed from the Northwest Territories as settlement in the Prairies increased.</li>
                        <li><strong>1949 – Newfoundland and Labrador:</strong> Joined after a close referendum, ending its time as a separate dominion.</li>
                        <li><strong>1999 – Nunavut:</strong> Created from the eastern Northwest Territories, giving the Inuit more self-governance and recognition of their cultural identity.</li>
                    </ul>
                    <p>✔ Key Fact: Canada’s borders and diversity grew gradually, often shaped by economic opportunity, transportation projects, and political agreements.</p>
                    <br>

                    <h3>3.2 The Canadian Pacific Railway (CPR)</h3>
                    <p>One of Canada’s greatest infrastructure achievements, the CPR connected the country from coast to coast:</p>
                    <ul>
                        <li><strong>Completed: 1885.</strong></li>
                        <li>Essential for trade, immigration settlement, and military transport.</li>
                        <li>Built largely with the labour of <strong>Chinese workers</strong>, who faced dangerous working conditions and later a discriminatory head tax.</li>
                        <li>Symbolized national unity and the ability to overcome vast geographic challenges.</li>
                    </ul>
                    <br>

                    <h3>3.3 Immigration and Settlement</h3>
                    <p>Between the late 1800s and early 1900s, Canada welcomed large waves of immigrants:</p>
                    <ul>
                        <li>Prairie Provinces attracted farmers from Ukraine, Poland, Germany, and Scandinavia.</li>
                        <li>Industrial cities saw Italian, Jewish, and other European immigrants arrive for factory jobs.</li>
                        <li>Immigration from Asia and Africa was heavily restricted until reforms after World War II.</li>
                    </ul>
                    <br>

                    <h3>3.4 Canada in World War I (1914–1918)</h3>
                    <ul>
                        <li>Canada entered the war automatically as part of the British Empire.</li>
                        <li>Over 600,000 Canadians served; more than 60,000 died.</li>
                        <li><strong>Battle of Vimy Ridge (1917)</strong> – A major Canadian victory that became a symbol of national pride and independence.</li>
                        <li>Women gained the right to vote in federal elections in 1918 (initially for women related to servicemen).</li>
                    </ul>
                    <br>

                    <h3>3.5 The Interwar Years</h3>
                    <ul>
                        <li>1920s: Economic growth, urbanization, and cultural development.</li>
                        <li><strong>1930s – The Great Depression:</strong> Widespread unemployment and hardship; led to the creation of early social welfare programs.</li>
                    </ul>
                    <br>

                    <h3>3.6 Canada in World War II (1939–1945)</h3>
                    <ul>
                        <li>Declared war on Germany independently of Britain.</li>
                        <li>Canadian forces fought at Dieppe (1942) and Juno Beach on D-Day (1944).</li>
                        <li>Industrial output rose sharply to supply the war effort.</li>
                        <li>Post-war, Canada welcomed refugees and displaced persons from Europe.</li>
                    </ul>
                    <br>

                    <h3>3.7 Post-War Growth and Social Change (1945–1970s)</h3>
                    <ul>
                        <li>Creation of <strong>universal healthcare (Medicare)</strong>.</li>
                        <li>Expansion of public education and transportation networks.</li>
                        <li>In <strong>1971</strong>, Canada became the first country to adopt <strong>multiculturalism</strong> as official policy.</li>
                    </ul>
                    <br>

                    <h3>3.8 The Constitution Act and the Charter of Rights and Freedoms</h3>
                    <ul>
                        <li><strong>1982:</strong> Canada’s Constitution was fully patriated from Britain.</li>
                        <li>The <strong>Charter of Rights and Freedoms</strong> was added, guaranteeing freedoms of speech, religion, mobility, and equality for all Canadians.</li>
                    </ul>
