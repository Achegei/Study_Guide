                    <h2>Chapter 2 – Canadian History: From Indigenous Roots to Confederation</h2>
                    <h3>2.1 Canada Before European Contact</h3>
                    <p>Long before Canada existed as a country, the land was home to diverse Indigenous peoples with their own languages, governance systems, spiritual beliefs, and cultural traditions. Archaeological evidence shows they have lived here for thousands of years, adapting to Canada’s varied climates and landscapes.</p>
                    <p>Main Indigenous groups:</p>
                    <ul>
                        <li><strong>First Nations</strong> – The largest and most diverse group, living from the Atlantic to the Pacific.
                            <ul>
                                <li>Woodland peoples built longhouses and hunted moose and deer.</li>
                                <li>Plains peoples followed bison herds and lived in portable tipis.</li>
                                <li>West Coast peoples, such as the Haida and Coast Salish, fished, carved totem poles, and built cedar longhouses.</li>
                            </ul>
                        </li>
                        <li><strong>Inuit</strong> – Inhabited the Arctic and subarctic, hunting seals, whales, and fish, and building igloos or sod houses. They mastered survival in extreme cold with specialized clothing and tools.</li>
                        <li><strong>Métis</strong> – A distinct people of mixed Indigenous and European ancestry, emerging during the fur trade. They developed the Michif language and unique cultural traditions.</li>
                    </ul>
                    <p>✔ Key Fact: Indigenous peoples had sophisticated trade networks long before Europeans arrived.</p>
                    <br>

                    <h3>2.2 Early European Exploration</h3>
                    <p>The first known Europeans to reach what is now Canada were the Norse, led by Leif Erikson around the year 1000, who briefly settled at L’Anse aux Meadows in Newfoundland.</p>
                    <p>Centuries later, exploration began in earnest:</p>
                    <ul>
                        <li><strong>John Cabot</strong> (Giovanni Caboto), an Italian sailing for England, explored parts of Newfoundland in 1497.</li>
                        <li><strong>Jacques Cartier</strong>, a French explorer, made three voyages (1534–1542), mapping the Gulf of St. Lawrence and claiming land for France.</li>
                        <li>Cartier recorded the word <strong>kanata</strong> from the Iroquoian people, meaning “village” or “settlement” — the origin of the name “Canada.”</li>
                    </ul>
                    <br>

                    <h3>2.3 The Fur Trade</h3>
                    <p>From the 1600s to the 1800s, the fur trade was Canada’s dominant economic driver:</p>
                    <ul>
                        <li>Beaver pelts were prized in Europe for hat-making.</li>
                        <li>Indigenous peoples trapped and prepared the furs, trading them for metal tools, blankets, and firearms.</li>
                        <li><strong>Hudson’s Bay Company (HBC)</strong> – Founded in 1670 with a royal charter, controlling Rupert’s Land.</li>
                        <li><strong>North West Company</strong> – A Montreal-based rival, more active in the interior.</li>
                        <li><strong>Voyageurs and coureurs de bois</strong> – Traders and paddlers who ventured deep inland to exchange goods with Indigenous communities.</li>
                    </ul>
                    <p>✔ Key Fact: The fur trade influenced settlement patterns and Indigenous-European relations for centuries.</p>
                    <br>

                    <h3>2.4 Rivalry Between France and Britain</h3>
                    <p>In the 17th and 18th centuries, France and Britain fought for dominance in North America:</p>
                    <ul>
                        <li><strong>New France</strong> – Settlements along the St. Lawrence River, Quebec City, Montreal, and Acadia.</li>
                        <li>British colonies – In Newfoundland, Nova Scotia, and later the Thirteen Colonies to the south.</li>
                        <li><strong>Seven Years’ War</strong> (1756–1763) – Decisive conflict in which the British defeated the French at the Battle of the Plains of Abraham (1759).</li>
                        <li><strong>Treaty of Paris</strong> (1763) – France ceded most of its North American territories to Britain.</li>
                        <li><strong>Quebec Act</strong> (1774) – Protected French language, Catholic religion, and civil law in Quebec.</li>
                    </ul>
                    <br>

                    <h3>2.5 Loyalists and Early Growth</h3>
                    <p>After the American Revolution (1775–1783), tens of thousands of Loyalists — colonists loyal to Britain — moved north to British North America:</p>
                    <ul>
                        <li>Many settled in Nova Scotia, Quebec, and the new colony of New Brunswick (1784).</li>
                        <li>In 1791, the Constitutional Act split Quebec into:
                            <ul>
                                <li>Upper Canada (now Ontario) – English-speaking, English law.</li>
                                <li>Lower Canada (now Quebec) – French-speaking, French civil law.</li>
                            </ul>
                        </li>
                    </ul>
                    <br>

                    <h3>2.6 Steps Toward Confederation</h3>
                    <p>Several events pushed the colonies toward unity:</p>
                    <ul>
                        <li><strong>War of 1812</strong> – Britain and its colonies resisted American invasions, boosting a shared identity.</li>
                        <li><strong>Rebellions of 1837–1838</strong> – Calls for more democratic government in Upper and Lower Canada.</li>
                        <li><strong>Act of Union (1841)</strong> – Merged Upper and Lower Canada into the Province of Canada.</li>
                        <li><strong>Responsible Government</strong> – Gave colonies more control over internal affairs.</li>
                        <li>Economic Cooperation – Trade and rail links encouraged unity.</li>
                    </ul>
                    <br>

                    <h3>2.7 Confederation – 1867</h3>
                    <p>On <strong>July 1, 1867</strong>, the British North America Act united four provinces into the Dominion of Canada:</p>
                    <ol>
                        <li>Ontario</li>
                        <li>Quebec</li>
                        <li>New Brunswick</li>
                        <li>Nova Scotia</li>
                    </ol>
                    <p>Canada gained control over domestic policy while remaining part of the British Empire. Over time, other provinces and territories joined to create modern Canada.</p>
                    <p>✔ Key Fact: Confederation was driven by economic needs, defense concerns, and the goal of uniting English- and French-speaking populations.</p>
                    <br>

