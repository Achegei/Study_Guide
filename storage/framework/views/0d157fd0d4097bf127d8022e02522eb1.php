                    <h2>Chapter 9 – Symbols of Canada</h2>
                    <h3>9.1 Why Symbols Matter</h3>
                    <p>National symbols are more than decorative designs — they express a country’s history, values, and identity. In Canada, they reflect Indigenous heritage, French and British traditions, and the multicultural character of the nation today.</p>
                    <p>✔ Key Fact: Many symbols became official only after years of popular use and national recognition.</p>
                    <br>

                    <h3>9.2 The National Flag</h3>
                    <ul>
                        <li><strong>Design:</strong> A red-and-white banner with a stylized red maple leaf in the centre.</li>
                        <li><strong>Adopted:</strong> February 15, 1965, replacing the Red Ensign.</li>
                        <li><strong>Meaning:</strong> The maple leaf has been a Canadian emblem for over 150 years, symbolizing unity, endurance, and the natural environment.</li>
                    </ul>
                    <p>📌 Test Tip: February 15 is National Flag of Canada Day.</p>
                    <br>

                    <h3>9.3 The Coat of Arms of Canada</h3>
                    <p>Canada’s coat of arms combines symbols of its heritage:</p>
                    <ul>
                        <li>English & Scottish Influence: Lions and the Union Jack.</li>
                        <li>French Influence: Fleur-de-lis.</li>
                        <li>Irish Influence: Harp of Ireland.</li>
                        <li>Canadian Identity: Three maple leaves beneath the shield represent the unity of the provinces and territories.</li>
                        <li><strong>Motto:</strong> <i>A Mari Usque Ad Mare</i> — “From Sea to Sea.”</li>
                    </ul>
                    <p>It appears on official documents, passports, and government buildings.</p>
                    <br>

                    <h3>9.4 The National Anthem – “O Canada”</h3>
                    <ul>
                        <li>Officially adopted in 1980.</li>
                        <li>Performed in English, French, or bilingual versions.</li>
                        <li>Played at major public events, including sports games and national ceremonies.</li>
                    </ul>
