                    <h2>Chapter 6 – Justice and the Law</h2>
                    <h3>6.1 The Rule of Law</h3>
                    <p>The foundation of Canada’s justice system is the <strong>rule of law</strong> — meaning that no one is above the law, not even the Prime Minister or other leaders.</p>
                    <p>The rule of law ensures:</p>
                    <ul>
                        <li><strong>Fairness</strong> – Everyone is treated equally in legal matters.</li>
                        <li><strong>Stability</strong> – Laws are applied consistently, not arbitrarily.</li>
                        <li><strong>Accountability</strong> – Citizens and governments must follow the same legal rules.</li>
                    </ul>
                    <p>✔ Key Fact: Government actions can be challenged in court if they violate the Constitution or other laws.</p>
                    <br>

                    <h3>6.2 Sources of Canadian Law</h3>
                    <p>Canadian law comes from several traditions:</p>
                    <ol>
                        <li><strong>English Common Law</strong> – Based on court decisions and legal precedents; applies in most of Canada.</li>
                        <li><strong>French Civil Law</strong> – Applies in Quebec for private law (contracts, property), based on written legal codes.</li>
                        <li><strong>Indigenous Legal Traditions</strong> – Laws and governance systems used by Indigenous peoples for centuries, increasingly recognized today.</li>
                        <li><strong>Statutory Law</strong> – Laws passed by Parliament or provincial/territorial legislatures.</li>
                        <li><strong>The Constitution</strong> – The highest law in Canada, which includes the Charter of Rights and Freedoms.</li>
                    </ol>
                    <br>

                    <h3>6.3 Law Enforcement in Canada</h3>
                    <p>Canada’s police services operate at three levels:</p>
                    <ul>
                        <li><strong>Municipal Police</strong> – Serve cities/towns and enforce local laws.</li>
                        <li><strong>Provincial Police</strong> – Operate in provinces without municipal forces (e.g., Ontario Provincial Police, Sûreté du Québec).</li>
                        <li><strong>Royal Canadian Mounted Police (RCMP)</strong> – The federal police force, also providing provincial policing in most provinces and territories.</li>
                    </ul>
                    <p>📌 Note: The RCMP’s famous red serge uniform is mainly used for ceremonies — daily policing is done in modern uniforms.</p>
                    <br>

                    <h3>6.4 The Court System</h3>
                    <p>Courts in Canada are independent from politics. Judges decide cases based on the law and evidence, not government orders.</p>
                    <p>Court hierarchy:</p>
                    <ol>
                        <li>Provincial/Territorial Courts – Handle most criminal and civil cases, including family law.</li>
                        <li>Federal Courts – Deal with federal law cases (e.g., immigration, taxation).</li>
                        <li>Courts of Appeal – Review lower court decisions for legal fairness.</li>
                        <li><strong>Supreme Court of Canada</strong> – The highest court; decisions are final and binding nationwide.</li>
                    </ol>
                    <p>📌 Test Tip: The Supreme Court has nine judges, including the Chief Justice of Canada.</p>
                    <br>

                    <h3>6.5 Criminal Law vs. Civil Law</h3>
                    <ul>
                        <li><strong>Criminal Law</strong> – Deals with offences against society (e.g., theft, assault, fraud). Punishments can include prison, fines, or community service.</li>
                        <li><strong>Civil Law</strong> – Resolves disputes between individuals or organizations (e.g., contracts, property issues). Remedies often involve payment or specific actions instead of jail.</li>
                    </ul>
                    <br>

                    <h3>6.6 Citizens’ Role in Justice</h3>
                    <p>Canadians help maintain justice by:</p>
                    <ul>
                        <li>Serving on Juries – Ensuring impartial verdicts.</li>
                        <li>Reporting Crimes – Helping police protect communities.</li>
                        <li>Respecting Court Decisions – Even when disagreeing with outcomes.</li>
                    </ul>
                    <br>

                    <h3>6.7 Legal Aid and Access to Justice</h3>
                    <p>Canadians who cannot afford a lawyer may qualify for legal aid — a publicly funded service ensuring fair access to the legal system.</p>
