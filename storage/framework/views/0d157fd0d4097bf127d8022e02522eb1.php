                    <h2>Chapter 1 – Canada and Citizenship</h2>
                    <h3>1.1 Understanding Canadian Citizenship</h3>
                    <p>Canadian citizenship is both a legal status and a shared national identity. It signifies belonging to a country built on freedom, democracy, equality, and the rule of law.</p>
                    <p>As a citizen, you gain rights and freedoms that allow you to live with dignity and opportunity — but you also take on responsibilities that sustain Canada’s fairness, stability, and prosperity.</p>
                    <p>It’s a two-way relationship:</p>
                    <ul>
                        <li>Canada offers protection, opportunity, and a secure society.</li>
                        <li>In return, you respect its laws, values, and traditions, and contribute to the community.</li>
                    </ul>
                    <p>Citizenship also connects you to the sacrifices and achievements of past generations, while committing you to leaving a better Canada for those who follow.</p>
                    <p>✔ Key Fact: Canada is both a <strong>constitutional monarchy</strong> (the King is the Head of State) and a <strong>parliamentary democracy</strong> (citizens elect representatives to govern).</p>
                    <br>

                    <h3>1.2 How Canadian Citizenship Evolved</h3>
                    <p>Before 1947, people living in Canada were legally British subjects. Over time, Canada’s identity developed into an independent nationality.</p>
                    <p>Key milestones:</p>
                    <ul>
                        <li><strong>1947 – Canadian Citizenship Act:</strong> Established Canadian citizenship as distinct from British nationality.</li>
                        <li><strong>1977 – Law Reform:</strong> Allowed dual citizenship and removed rules that caused loss of citizenship for Canadians living abroad or marrying non-Canadians.</li>
                        <li><strong>Today:</strong> Citizenship is granted by birth in Canada, by birth abroad to Canadian parents, or by naturalization for eligible permanent residents.</li>
                    </ul>
                    <br>

                    <h3>1.3 Rights of Canadian Citizens</h3>
                    <p>Canadian citizens enjoy specific rights not available to permanent or temporary residents.</p>
                    <p>Core rights include:</p>
                    <ol>
                        <li><strong>Vote in Elections</strong> – Federal, provincial/territorial, and municipal.</li>
                        <li><strong>Run for Public Office</strong> – Become a candidate in elections.</li>
                        <li><strong>Hold a Canadian Passport</strong> – Provides visa-free access to many countries and diplomatic support abroad.</li>
                        <li><strong>Equality Before the Law</strong> – Protection from discrimination and equal treatment under Canadian law.</li>
                        <li><strong>Language Rights</strong> – Access to federal services in English or French, anywhere in Canada.</li>
                    </ol>
                    <p>📌 Test Tip: <strong>Only citizens can vote in federal elections.</strong> Permanent residents cannot.</p>
                    <br>

                    <h3>1.4 Responsibilities of Canadian Citizens</h3>
                    <p>With citizenship comes the duty to uphold Canada’s values and maintain its stability.</p>
                    <p>Core responsibilities:</p>
                    <ul>
                        <li><strong>Obey the Law</strong> – Applies equally to everyone.</li>
                        <li><strong>Serve on a Jury</strong> – Ensures fairness in court trials.</li>
                        <li><strong>Vote</strong> – Keeps democracy strong.</li>
                        <li><strong>Protect the Environment</strong> – Preserve natural resources for future generations.</li>
                        <li><strong>Defend Canada</strong> – Contribute to national protection when required.</li>
                    </ul>
                    <br>

                    <h3>1.5 The Oath of Citizenship</h3>
                    <p>The final step in becoming a citizen is taking the Oath of Citizenship during a public ceremony. In the oath, you:</p>
                    <ul>
                        <li>Swear loyalty to His Majesty King Charles III, King of Canada.</li>
                        <li>Promise to respect Canada’s laws.</li>
                        <li>Commit to the responsibilities of citizenship.</li>
                    </ul>
                    <p>This is both a legal requirement and a symbolic commitment to the Canadian family.</p>
                    <br>

                    <h3>1.6 Canadian Values</h3>
                    <p>Canada’s identity is shaped by shared values:</p>
                    <ul>
                        <li><strong>Freedom</strong> – Live and express yourself without fear, within the law.</li>
                        <li><strong>Democracy</strong> – Leaders chosen in fair, free, and regular elections.</li>
                        <li><strong>Equality</strong> – Equal treatment for all, regardless of background.</li>
                        <li><strong>Human Rights</strong> – Protection from discrimination and injustice.</li>
                        <li><strong>Multiculturalism</strong> – Embracing diversity as a strength.</li>
                        <li><strong>Peaceful Coexistence</strong> – Resolving disagreements without violence.</li>
                    </ul>
                    <br>

                    <h3>1.7 Pathways to Citizenship</h3>
                    <p>Most newcomers become citizens through naturalization:</p>
                    <ol>
                        <li>Become a permanent resident.</li>
                        <li>Live in Canada for at least 1,095 days in the past 5 years.</li>
                        <li>Demonstrate basic English or French ability.</li>
                        <li>Pass the citizenship test.</li>
                        <li>Have no serious criminal record.</li>
                        <li>Attend the citizenship ceremony and take the oath.</li>
                    </ol>
                    <p>Others are citizens automatically by birth — either in Canada or abroad to Canadian parents.</p>
                    <br>

                    <h3>1.8 Why Citizenship Matters</h3>
                    <p>Canadian citizenship provides:</p>
                    <ul>
                        <li>A voice in shaping Canada’s future through voting.</li>
                        <li>Access to certain careers (e.g., government jobs with security clearance).</li>
                        <li>Stronger travel rights and protections abroad.</li>
                        <li>A lifelong bond to Canada’s people, land, and values.</li>
                    </ul>
