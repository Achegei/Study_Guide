                    <h2>Chapter 7 – Canada’s Economy and Work</h2>
                    <h3>7.1 Overview of Canada’s Economy</h3>
                    <p>Canada has one of the strongest and most stable economies in the world, built on abundant natural resources, a skilled workforce, advanced technology, and global trade.</p>
                    <p>It is a mixed-market economy, meaning:</p>
                    <ul>
                        <li>Businesses operate freely in most sectors.</li>
                        <li>The government regulates certain areas (e.g., healthcare, environment, worker rights) to protect the public interest.</li>
                    </ul>
                    <p>✔ Key Fact: Canada’s economy is divided into three main sectors:</p>
                    <ol>
                        <li><strong>Primary</strong> – Natural resources (mining, forestry, farming, fishing).</li>
                        <li><strong>Secondary</strong> – Manufacturing and processing.</li>
                        <li><strong>Tertiary</strong> – Services (healthcare, finance, education, retail, tourism).</li>
                    </ol>
                    <br>

                    <h3>7.2 Natural Resources – Canada’s Economic Foundation</h3>
                    <p>Natural resources have driven Canada’s growth for centuries, from the fur trade to modern energy and mining industries.</p>
                    <p>Major resource industries:</p>
                    <ul>
                        <li><strong>Oil and Gas</strong> – Alberta, Saskatchewan, and Newfoundland and Labrador are leading producers.</li>
                        <li><strong>Mining</strong> – Canada is a top supplier of gold, nickel, uranium, potash, and diamonds.</li>
                        <li><strong>Forestry</strong> – British Columbia, Quebec, and Ontario export lumber and paper worldwide.</li>
                        <li><strong>Fisheries</strong> – The Atlantic provinces and British Columbia rely on sustainable fishing industries.</li>
                    </ul>
                    <p>📌 Test Tip: Canada is among the world’s top oil producers.</p>
                    <br>

                    <h3>7.3 Agriculture – Feeding Canada and Beyond</h3>
                    <p>Agriculture plays a major role, especially in the Prairie provinces.</p>
                    <p>Key agricultural outputs:</p>
                    <ul>
                        <li><strong>Wheat and Canola</strong> – Saskatchewan and Alberta lead production.</li>
                        <li><strong>Dairy and Poultry</strong> – Quebec is the largest dairy producer.</li>
                        <li><strong>Fruit and Vegetables</strong> – Ontario’s Niagara region and BC’s Okanagan Valley are famous for orchards and vineyards.</li>
                    </ul>
                    <p>Canadian agricultural exports bring billions in revenue annually.</p>
                    <br>

                    <h3>7.4 Manufacturing and Industry</h3>
                    <p>Manufacturing supports hundreds of thousands of jobs.</p>
                    <p>Key manufacturing sectors:</p>
                    <ul>
                        <li><strong>Automotive</strong> – Ontario builds vehicles for domestic and export markets.</li>
                        <li><strong>Aerospace</strong> – Quebec and Ontario produce aircraft and parts.</li>
                        <li><strong>Shipbuilding</strong> – Important in British Columbia, Nova Scotia, and Quebec.</li>
                    </ul>
                    <br>

                    <h3>7.5 The Service Sector – Canada’s Largest Employer</h3>
                    <p>Over three-quarters of Canadians work in service industries such as:</p>
                    <ul>
                        <li>Healthcare and education</li>
                        <li>Finance and banking</li>
                        <li>Information technology and telecommunications</li>
                        <li>Tourism and hospitality</li>
                    </ul>
                    <p>✔ Key Fact: The service sector is now the largest part of Canada’s economy.</p>
                    <br>

                    <h3>7.6 International Trade</h3>
                    <p>Trade is essential to Canada’s prosperity.</p>
                    <p>Main trade facts:</p>
                    <ul>
                        <li><strong>Largest partner:</strong> United States (via the USMCA trade agreement).</li>
                        <li>Other partners: China, European Union, Japan, South Korea.</li>
                        <li>Major exports: Oil, gas, minerals, lumber, wheat, seafood, cars.</li>
                        <li>Major imports: Machinery, electronics, vehicles, consumer goods.</li>
                    </ul>
                    <br>

                    <h3>7.7 Workers’ Rights</h3>
                    <p>Labour laws protect employees by ensuring:</p>
                    <ul>
                        <li>Minimum wage (varies by province/territory).</li>
                        <li>Limits on working hours and guaranteed overtime pay.</li>
                        <li>Workplace safety standards.</li>
                        <li>The right to join a union and bargain collectively.</li>
                    </ul>
                    <p>Unions negotiate fair wages, benefits, and working conditions.</p>
                    <br>

                    <h3>7.8 Innovation and Technology</h3>
                    <p>Canada invests heavily in research and development:</p>
                    <ul>
                        <li>Artificial intelligence hubs in Toronto, Montreal, and Edmonton.</li>
                        <li>Clean energy projects in wind, solar, and hydroelectric power.</li>
                        <li>Advances in medical research and biotechnology.</li>
                    </ul>
                    <br>

                    <h3>7.9 Challenges and Opportunities</h3>
                    <p>Challenges: Global competition, fluctuating resource prices, climate change, automation.
                    Opportunities: Growth in green technology, diversified trade, and tech innovation.</p>
                    <br>

